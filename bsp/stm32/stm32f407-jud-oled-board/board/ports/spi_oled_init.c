#include <rtthread.h>
#include "drv_spi.h"

static int rt_hw_spi_oled_init(void)
{
    __HAL_RCC_GPIOA_CLK_ENABLE();
    rt_hw_spi_device_attach("spi1", "spi10", GPIOA, GPIO_PIN_4);

    return RT_EOK;
}
INIT_COMPONENT_EXPORT(rt_hw_spi_oled_init);

