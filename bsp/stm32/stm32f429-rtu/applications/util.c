/*************************************************
* Function: util_str_to_u32
* Description: 字符串转32位无符号整数
* Author: wangk
* Returns:
* Parameter:
* History:
*************************************************/
uint32_t util_str_to_u32(const char* str, uint32_t str_len)
{
    if ((NULL == str) 
        || (str_len <= 0))
    {
        return 0;
    }
    
    uint32_t val = 0;
    uint32_t i = 0;
    for (i = 0; i < str_len; ++i)
    {
        char ch = str[i];
        if (!isdigit(ch))
        {
            break;
        }
        val = val * 10 + (ch - '0');
    }

    return val;
}

